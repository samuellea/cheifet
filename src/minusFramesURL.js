const fs = require('fs');

function updateData(firstArray, secondArray) {
  // Iterate over each object in the first array (A)
  firstArray.forEach((A) => {
    // Find the corresponding object (B) in the second array
    const matchingB = secondArray.find(
      (B) => Array.isArray(B.titles) && B.titles.includes(A.title)
    );

    if (matchingB) {
      // If the .links array in A is not empty, update B
      if (Array.isArray(A.links) && A.links.length > 0) {
        // Update .internetArchiveURL of B with A's .links
        matchingB.internetArchiveURL = A.links;

        // Iterate over the .links array of A and update .framesURL of B
        matchingB.framesURL = A.links.map((link) => {
          // Extract the portion of the link after 'details/'
          const detailPart = link.split('details/')[1];

          // Return the new URL for the framesURL array
          return `https://api.gdeltproject.org/api/v2/tvv/tvv?id=IAITEM_${detailPart}`;
        });
      } else {
        // If A's .links array is empty, set .internetArchiveURL and .framesURL to empty arrays
        matchingB.internetArchiveURL = [];
        matchingB.framesURL = [];
      }
    }
  });

  // Prepare the output as a string that assigns the modified array to a variable
  const outputData = `const allIntArchsAndFrames = ${JSON.stringify(
    secondArray,
    null,
    2
  )};\n`;

  // Write the modified array to a file called 'allIntArchsAndFrames.js'
  fs.writeFile('allIntArchsAndFrames.js', outputData, (err) => {
    if (err) {
      console.error('Error writing to file:', err);
    } else {
      console.log('File saved as allIntArchsAndFrames.js');
    }
  });
}

